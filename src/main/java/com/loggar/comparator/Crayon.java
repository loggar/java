package com.loggar.comparator;

public class Crayon {
	private String color;

	public Crayon(String color) {
		this.color = color;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String toString() {
		return this.color;
	}
}
